Qu'est-ce qu'un cookie ?<br>
Un cookie est un petit fichier de données qui est placé sur votre ordinateur ou appareil mobile lorsque vous visitez un site web. Les cookies sont largement utilisés par les propriétaires de sites web pour faire fonctionner leurs sites, ou pour les faire fonctionner plus efficacement, ainsi que pour fournir des informations de rapport.
<br><br>
Les cookies fixés par le propriétaire du site web (dans ce cas, BLOM) sont appelés "cookies de première partie". Les cookies fixés par des parties autres que le propriétaire du site web sont appelés "cookies tiers". Les cookies tiers permettent de fournir des fonctionnalités ou des fonctionnalités tierces sur ou via le site web (par exemple, comme la publicité, le contenu interactif et les analyses). Les parties qui fixent ces cookies tiers peuvent reconnaître votre ordinateur lorsqu'il visite le site web en question et également lorsqu'il visite certains autres sites web.
<br><br>
Pourquoi utilisons-nous des cookies ?<br>
Nous utilisons des cookies de première partie et des cookies tiers pour plusieurs raisons. Certains cookies sont nécessaires pour des raisons techniques afin que notre site web fonctionne, et nous les appelons cookies "essentiels" ou "strictement nécessaires". D'autres cookies nous permettent de suivre et de cibler les intérêts de nos utilisateurs pour améliorer l'expérience sur notre site web. Les tiers utilisent des cookies via notre site web à des fins de publicité, d'analyses et autres.
